def solve(reader: io.TextIOBase) -> int:
    t = 0
    reg = 1
    total = 0

    def tick():
        nonlocal t, reg, total
        t += 1
        if t % 40 == 20:
            total += reg * t

    for line in reader.readlines():
        match line.split():
            case ["noop"]:
                tick()
            case ["addx", value]:
                tick()
                tick()
                reg += int(value)

    return total


assert solve(io.StringIO(EXAMPLE_INPUT)) == 13140


def main():
    with open("input/day10.txt") as file:
        solution = solve(file)
        print(f"The sum of the signal strengths is {solution}.")


